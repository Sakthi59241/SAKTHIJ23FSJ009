package simple_problems;

public class SwitchCase {
	public static void main(String[] args) {
		int tech = 10;

		switch(tech){
		case 1:
		 {
		 System.out.println("C++");
		 break;
		 }
		 case 2:{
		 System.out.println("java");
		 break;
		 }
		 case 3:{
		 System.out.println("python");
		 break;
		}
		 default:{
		 System.out.println("Not Listed..");
		 }
	 } 
  }
} 

